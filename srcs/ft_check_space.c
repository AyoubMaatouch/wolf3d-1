#include "cub3d.h"

int		ft_check_space(size_t j, char *str)
{
	size_t	k;

	k = ft_strlen(str) - 1;
	if (j < 0 || k < j || str[j] == ' ' || str[j] == 0)
		return (1);
	return (0);
}
